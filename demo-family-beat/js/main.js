function initVippsModal() {
  const backdrop = document.getElementById('vipps-modal');
  if (!backdrop) return;

  function openModal() {
    backdrop.classList.add('open');
    document.body.style.overflow = 'hidden';
    backdrop.querySelector('.modal-close-btn').focus();
  }

  function closeModal() {
    backdrop.classList.remove('open');
    document.body.style.overflow = '';
  }

  // Trigger: all booking buttons
  document.querySelectorAll('.vipps-trigger').forEach(btn => {
    btn.addEventListener('click', openModal);
  });

  // Close button
  const closeBtn = backdrop.querySelector('.modal-close-btn');
  if (closeBtn) closeBtn.addEventListener('click', closeModal);

  // Close on backdrop click
  backdrop.addEventListener('click', e => {
    if (e.target === backdrop) closeModal();
  });

  // Close on Escape
  document.addEventListener('keydown', e => {
    if (e.key === 'Escape' && backdrop.classList.contains('open')) closeModal();
  });
}

// === CALENDAR (booking page) ===
const SEASON_START_MONTH = 4;  // May (0-indexed)
const SEASON_START_DAY   = 15;
const SEASON_END_MONTH   = 8;  // September (0-indexed)
const SEASON_END_DAY     = 15;

// Hardcoded booked dates (YYYY-MM-DD)
const BOOKED_DATES = new Set([
  '2026-06-12', '2026-06-13', '2026-06-20', '2026-06-21',
  '2026-07-04', '2026-07-05', '2026-07-18', '2026-07-19',
  '2026-08-01', '2026-08-08', '2026-08-09',
  '2026-09-06', '2026-09-07',
]);

let calYear, calMonth;

function isInSeason(year, month, day) {
  const date = new Date(year, month, day);
  const start = new Date(year, SEASON_START_MONTH, SEASON_START_DAY);
  const end   = new Date(year, SEASON_END_MONTH, SEASON_END_DAY);
  return date >= start && date <= end;
}

function formatDate(year, month, day) {
  return `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
}

function renderCalendar() {
  const grid = document.getElementById('calendar-grid');
  const title = document.getElementById('calendar-title');
  if (!grid || !title) return;

  const today = new Date();
  const firstDay = new Date(calYear, calMonth, 1);
  const daysInMonth = new Date(calYear, calMonth + 1, 0).getDate();
  // Monday-based: 0=Mon, 6=Sun
  let startOffset = firstDay.getDay() - 1;
  if (startOffset < 0) startOffset = 6;

  const monthNames = {
    no: ['Januar','Februar','Mars','April','Mai','Juni','Juli','August','September','Oktober','November','Desember'],
    en: ['January','February','March','April','May','June','July','August','September','October','November','December']
  };

  title.textContent = `${monthNames[currentLang][calMonth]} ${calYear}`;

  grid.innerHTML = '';

  // Empty cells for offset
  for (let i = 0; i < startOffset; i++) {
    const empty = document.createElement('div');
    empty.className = 'cal-day empty';
    grid.appendChild(empty);
  }

  for (let day = 1; day <= daysInMonth; day++) {
    const cell = document.createElement('div');
    cell.textContent = day;

    const dateStr = formatDate(calYear, calMonth, day);
    const isToday = (calYear === today.getFullYear() && calMonth === today.getMonth() && day === today.getDate());
    const inSeason = isInSeason(calYear, calMonth, day);
    const isBooked = BOOKED_DATES.has(dateStr);

    if (isBooked) {
      cell.className = 'cal-day booked';
      cell.setAttribute('aria-label', dateStr + ' - opptatt');
    } else if (inSeason) {
      cell.className = 'cal-day available';
      cell.setAttribute('role', 'button');
      cell.setAttribute('tabindex', '0');
      cell.setAttribute('aria-label', dateStr);
      cell.addEventListener('click', () => {
        document.getElementById('vipps-modal')?.classList.add('open');
        document.body.style.overflow = 'hidden';
        document.querySelector('#vipps-modal .modal-close-btn')?.focus();
      });
      cell.addEventListener('keydown', e => {
        if (e.key === 'Enter' || e.key === ' ') cell.click();
      });
    } else {
      cell.className = 'cal-day outside-season';
      cell.setAttribute('aria-label', dateStr + ' - utenom sesong');
    }

    if (isToday) cell.classList.add('today');
    grid.appendChild(cell);
  }
}

function initCalendar() {
  const grid = document.getElementById('calendar-grid');
  if (!grid) return;

  const today = new Date();
  calYear  = today.getFullYear();
  calMonth = today.getMonth();

  renderCalendar();

  document.getElementById('cal-prev')?.addEventListener('click', () => {
    calMonth--;
    if (calMonth < 0) { calMonth = 11; calYear--; }
    renderCalendar();
  });

  document.getElementById('cal-next')?.addEventListener('click', () => {
    calMonth++;
    if (calMonth > 11) { calMonth = 0; calYear++; }
    renderCalendar();
  });
}

