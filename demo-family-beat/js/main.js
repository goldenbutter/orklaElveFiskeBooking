// === LANGUAGE MANAGEMENT ===
let currentLang = localStorage.getItem('ef-lang') || 'no';

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) ||
         (translations['no'][key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'IMG') {
      el.alt = val;
    } else if (el.hasAttribute('placeholder')) {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });

  // Update html lang attribute
  document.documentElement.lang = currentLang === 'no' ? 'nb' : 'en';

  // Update lang toggle button
  const toggleEl = document.getElementById('lang-toggle');
  const mobileToggleEl = document.getElementById('mobile-lang-toggle');
  if (toggleEl) toggleEl.textContent = t('nav.lang');
  if (mobileToggleEl) mobileToggleEl.textContent = t('nav.lang');

  // Re-render calendar if present
  if (document.getElementById('calendar-grid')) renderCalendar();

  // Update calendar legend text
  const legAvail = document.querySelector('.legend-available-text');
  const legBooked = document.querySelector('.legend-booked-text');
  const legToday = document.querySelector('.legend-today-text');
  if (legAvail) legAvail.textContent = t('cal.legend.available');
  if (legBooked) legBooked.textContent = t('cal.legend.booked');
  if (legToday) legToday.textContent = t('cal.legend.today');
}

function toggleLanguage() {
  currentLang = currentLang === 'no' ? 'en' : 'no';
  localStorage.setItem('ef-lang', currentLang);
  applyTranslations();
}

function initLangToggle() {
  const btn = document.getElementById('lang-toggle');
  const mobileBtn = document.getElementById('mobile-lang-toggle');
  if (btn) btn.addEventListener('click', toggleLanguage);
  if (mobileBtn) mobileBtn.addEventListener('click', toggleLanguage);
}

// === NAVIGATION ===
function initNav() {
  // Scroll shadow
  const nav = document.querySelector('.nav');
  if (nav) {
    window.addEventListener('scroll', () => {
      nav.classList.toggle('scrolled', window.scrollY > 10);
    }, { passive: true });
  }

  // Hamburger
  const hamburger = document.getElementById('hamburger');
  const mobileMenu = document.getElementById('mobile-menu');

  if (hamburger && mobileMenu) {
    hamburger.addEventListener('click', () => {
      const isOpen = mobileMenu.classList.toggle('open');
      hamburger.classList.toggle('open', isOpen);
      hamburger.setAttribute('aria-expanded', isOpen);
      document.body.style.overflow = isOpen ? 'hidden' : '';
    });

    // Close on nav link click
    mobileMenu.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', () => {
        mobileMenu.classList.remove('open');
        hamburger.classList.remove('open');
        hamburger.setAttribute('aria-expanded', 'false');
        document.body.style.overflow = '';
      });
    });

    // Close on backdrop click
    mobileMenu.addEventListener('click', e => {
      if (e.target === mobileMenu) {
        mobileMenu.classList.remove('open');
        hamburger.classList.remove('open');
        hamburger.setAttribute('aria-expanded', 'false');
        document.body.style.overflow = '';
      }
    });
  }

  // Active nav link
  const currentPage = window.location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('.nav-links a, .mobile-menu a').forEach(link => {
    const href = link.getAttribute('href');
    if (href && href === currentPage) link.classList.add('active');
    if (currentPage === '' && href === 'index.html') link.classList.add('active');
  });
}

// === SCROLL ANIMATIONS ===
function initScrollAnimations() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
}

